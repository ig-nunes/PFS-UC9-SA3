/* --------------Desktop-------------- */



function mostrarResposta1Desktop() {
    let resposta1desktop = document.querySelector("#resposta1-desktop");

    if (getComputedStyle(resposta1desktop).display == "none") {
        resposta1desktop.style.display = "flex";
    }else{
        resposta1desktop.style.display = "none";
    }             
}


